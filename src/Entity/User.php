    /**
     * @ORM\OneToMany(targetEntity=Compte::class, mappedBy="createdBy")
     */
    private $User;

    /**
     * @ORM\OneToMany(targetEntity=Tontine::class, mappedBy="createdBy")
     */
    private $tontines;

    /**
     * @ORM\OneToMany(targetEntity=Operation::class, mappedBy="createdBy")
     */
