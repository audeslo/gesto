     */
    private $slug;

    /**
     * @ORM\Column(type="string", length=255, nullable=true)
     */
    private $telephone;

    /**
     * @ORM\Column(type="string", length=180, unique=true)
     */
    private $username;

    /**
     * @ORM\Column(type="string", length=255, nullable=true)
     */
    private $usernameCanonical;

    /**
     *
     * @ORM\Column(type="string", length=255)
     * @Assert\NotBlank(message="Veuillez saisir le mail !")
     */
    private $email;

    /**
     * @ORM\Column(type="string", length=255, nullable=true)
     */
